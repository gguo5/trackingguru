    private void btn_uploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_uploadActionPerformed
        // TODO add your handling code here:
        info_label.setText("");
        info_label.setForeground(Color.BLACK);
        
        String host = fieldHost.getText();
        int port = Integer.parseInt(fieldPort.getText());
        String username = fieldUser.getText();
        String password = new String(fieldPass.getPassword());
        String uploadPath = fieldUploadPath.getText();
        String filePath = fieldFileName.getText();

        File uploadFile = new File(filePath);
        progressBar.setValue(0);
        UploadTask task = new UploadTask(host, port, username, password,
                uploadPath, uploadFile, this);
        task.addPropertyChangeListener(this);
        task.execute();
    }//GEN-LAST:event_btn_uploadActionPerformed


